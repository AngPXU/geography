/** Tìm tỉnh theo tên (alias matching — hỗ trợ cả tên cũ trước sáp nhập) */
export function findProvinceByName(name: string): VnProvince | null {
  if (!name) return null;
  const lower = name.toLowerCase().trim();

  // Chuẩn hoá: bỏ "tỉnh", "thành phố", "thủ đô", "tp." prefix
  const normalized = lower
    .replace(/^(tỉnh|thành phố|thủ đô|tp\.|city of|province of)\s+/i, '')
    .trim();

  for (const p of VN_PROVINCES) {
    // So khớp aliases
    for (const alias of p.aliases) {
      const aliasLower = alias.toLowerCase();
      if (aliasLower === lower || aliasLower === normalized) return p;
      if (lower.includes(aliasLower) && aliasLower.length > 4) return p;
    }
    // So sánh tên chính (bỏ prefix loại ĐVHC)
    const nameLower = p.name.toLowerCase().replace(/^(tỉnh|thành phố|thủ đô)\s+/, '');
    if (nameLower === normalized || normalized.includes(nameLower) || nameLower.includes(normalized)) return p;
  }
  return null;
}
